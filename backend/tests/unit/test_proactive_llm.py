@pytest.mark.asyncio
async def test_resolve_falls_back_to_template_on_llm_error():
    svc = InnerStateService()
    router = MagicMock()
    router.call_cheap = AsyncMock(side_effect=RuntimeError("boom"))
    with (
        patch.object(settings, "proactive_llm_enabled", True),
        patch("heart.infra.llm_providers.get_model_router", AsyncMock(return_value=router)),
    ):
        content = await svc._resolve_proactive_content(
            character_id="rin",
            trigger_type="scheduled",
            relationship_stage="FRIEND",
            intimacy=0.4,
            days_since_last_interaction=1.0,
            recent_context="",
            user_facts="",
        )
    assert content in get_proactive_templates("rin")


