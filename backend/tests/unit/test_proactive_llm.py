@pytest.mark.asyncio
async def test_tick_emits_llm_content_when_sending():
    """When a proactive fires, the message content comes from the LLM."""
    svc = InnerStateService()
    router = _mock_router("今天的风让我想起你。")
    with (
        patch.object(settings, "proactive_llm_enabled", True),
        patch("heart.infra.llm_providers.get_model_router", AsyncMock(return_value=router)),
        # Force the probabilistic send gate to fire.
        patch("heart.ss06_inner_state.service.random.random", return_value=0.0),
        patch("heart.ss06_inner_state.service.random.uniform", return_value=0.0),
    ):
        msg = await svc.tick(
            uuid4(),
            "rin",
            relationship_stage="FRIEND",
            intimacy=0.8,
            days_since_last_interaction=2.0,
        )
    assert msg is not None
    assert msg.content == "今天的风让我想起你。"
