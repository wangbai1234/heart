"""
Proactive Scheduler — SS06 Inner State & Behavior §3.9, §10.1, §10.7

Redis ZSET-backed scheduling for proactive messages generated by the Initiative
Decider → Proactive Message Generator pipeline.  Receives a PendingInitiative,
inserts it into a Redis sorted set ordered by scheduled_at, and provides a
background worker (ProactiveSender) that polls due items and dispatches them
to the notification/conversation layer.

Key invariants:
  - INV-I-2: scheduled_at ∉ quiet_hours ∧ interval ≥ MIN_PROACTIVE_GAP
  - I-4: Proactive must obey quiet hours + frequency cap
  - Idempotency: never double-fire the same initiative

Architecture:
  Redis ZSET  proactive:queue:{user_id}:{character_id}   score = scheduled_at
  Redis SET   proactive:sent:{user_id}:{character_id}    idempotency guard
  Redis STRING proactive:lock:{initiative_id}            dispatch mutex

Spec:   runtime_specs/06_inner_state_behavior_runtime.md §3.9, §5.3, §10.1, §10.7
Author: 心屿团队
"""

from __future__ import annotations

import json
