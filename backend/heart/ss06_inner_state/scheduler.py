def create_initiative(
    user_id: UUID,
    character_id: str,
    initiative_type: str,
    generated_message: str,
    scheduled_at: datetime | None = None,
    apply_jitter: bool = True,
    context: Dict[str, Any] | None = None,
) -> PendingInitiative:
    """Factory for building a PendingInitiative ready for scheduling.

    If *scheduled_at* is None, defaults to "now".
    *apply_jitter* controls whether scheduled_with_jitter gets a ±5 min offset.
    """
    now = datetime.now(timezone.utc)
    base = scheduled_at or now
    jittered = _add_jitter(base) if apply_jitter else base

    return PendingInitiative(
        initiative_id=str(uuid4()),
        user_id=user_id,
        character_id=character_id,
        initiative_type=initiative_type,
        generated_message=generated_message,
        scheduled_at=base,
        scheduled_with_jitter=jittered,
        context=context or {},
        created_at=now,
    )
